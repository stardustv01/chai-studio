import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const diagnosticsSourcePath = path.join(root, "packages/schema/src/source/diagnostic-category.schema.json");
const diagnosticsGeneratedPath = path.join(root, "packages/schema/src/generated/diagnostic-categories.ts");
const rationalSourcePath = path.join(root, "packages/schema/src/source/rational.schema.json");
const rationalGeneratedPath = path.join(root, "packages/schema/src/generated/rational-schema.ts");
const documentsSourcePath = path.join(root, "packages/schema/src/source/project-documents.schema.json");
const documentsGeneratedPath = path.join(root, "packages/schema/src/generated/project-document-schemas.ts");
const commandSourcePath = path.join(root, "packages/schema/src/source/command-envelope.schema.json");
const commandGeneratedPath = path.join(root, "packages/schema/src/generated/command-envelope-schema.ts");
const diagnosticsSchema = JSON.parse(await readFile(diagnosticsSourcePath, "utf8"));
const rationalSchema = JSON.parse(await readFile(rationalSourcePath, "utf8"));
const projectDocumentSchemaBundle = JSON.parse(await readFile(documentsSourcePath, "utf8"));
const commandEnvelopeSchema = JSON.parse(await readFile(commandSourcePath, "utf8"));
const values = diagnosticsSchema.enum;
if (!Array.isArray(values) || values.some((value) => typeof value !== "string")) {
  throw new Error("Diagnostic category source schema must contain a string enum.");
}

const artifacts = [
  {
    path: diagnosticsGeneratedPath,
    rendered: `// Generated by scripts/generate-schema-validator.mjs. Do not edit by hand.\nexport const generatedDiagnosticCategories = [\n${values.map((value) => `  ${JSON.stringify(value)},`).join("\n")}\n] as const;\n\nexport type GeneratedDiagnosticCategory = (typeof generatedDiagnosticCategories)[number];\n\nexport const isGeneratedDiagnosticCategory = (value: unknown): value is GeneratedDiagnosticCategory =>\n  typeof value === "string" && generatedDiagnosticCategories.includes(value as GeneratedDiagnosticCategory);\n`,
  },
  {
    path: rationalGeneratedPath,
    rendered: `// Generated by scripts/generate-schema-validator.mjs. Do not edit by hand.\nexport const rationalJsonSchema = ${JSON.stringify(rationalSchema, null, 2)} as const;\n`,
  },
  {
    path: documentsGeneratedPath,
    rendered: `// Generated by scripts/generate-schema-validator.mjs. Do not edit by hand.\nexport const projectDocumentSchemaBundle = ${JSON.stringify(projectDocumentSchemaBundle, null, 2)} as const;\n`,
  },
  {
    path: commandGeneratedPath,
    rendered: `// Generated by scripts/generate-schema-validator.mjs. Do not edit by hand.\nexport const commandEnvelopeJsonSchema = ${JSON.stringify(commandEnvelopeSchema, null, 2)} as const;\n`,
  },
];

if (process.argv.includes("--write")) {
  for (const artifact of artifacts) {
    await writeFile(artifact.path, artifact.rendered);
    console.log(`Generated ${path.relative(root, artifact.path)}`);
  }
} else if (process.argv.includes("--check")) {
  for (const artifact of artifacts) {
    const current = await readFile(artifact.path, "utf8");
    if (current !== artifact.rendered)
      throw new Error(
        `Generated schema artifact is stale: ${path.relative(root, artifact.path)}. Run pnpm schema:generate and review the diff.`,
      );
  }
  console.log(`Generated schema artifacts are current (${artifacts.length}).`);
} else {
  throw new Error("Use --write or --check.");
}
